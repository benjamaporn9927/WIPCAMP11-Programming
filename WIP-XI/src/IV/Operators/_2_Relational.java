public class _2_Relational {
    public static void main(String[] args) {
        int x = 66;
        
        System.out.println("== : " + (x == 11));
        
        System.out.println("!= : " + (x != 10));
        
        System.out.println("> : " + (x > 7));
        
        System.out.println("< : " + (x < 0));
        
        System.out.println(">= : " + (x >= 9));
        
        System.out.println("<= : " + (x <= 4));
    }
}
